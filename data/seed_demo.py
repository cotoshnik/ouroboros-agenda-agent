def main():
    date = sys.argv[1] if len(sys.argv) > 1 else "2026-07-19"
    with open(os.path.join(BASE_DIR, "memory", "knowledge", "indicators.json"), encoding="utf-8") as f:
        catalog = json.load(f)
    names = {i["id"]: (i["name"], "base" if i["base_block"] else "dynamic") for i in catalog["indicators"]}

    observations = []
    for indicator_id, (value, unit, source) in sorted(DEMO.items()):
        name, block = names[indicator_id]
        observations.append({
            "indicator_id": indicator_id, "name": name, "block": block,
            "value": value, "unit": unit, "source": source,
            "collected_at": datetime.now().isoformat(timespec="seconds"),
        })
    payload = {"date": date, "demo": True, "observations": observations}
    path = os.path.join(DATA_DIR, "observations_%s.json" % date)
    with open(path, "w", encoding="utf-8") as f:
        json.dump(payload, f, ensure_ascii=False, indent=2)
    print("Записано %d наблюдений за %s -> %s" % (len(observations), date, path))


if __name__ == "__main__":
    main()
